# Зчитування року з терміналу
year = int(input("Введіть рік: "))

zodiac_animals = [
    "Коза", "Мавпа", "Півень", "Собака", 
    "Свиня", "Щур", "Бик", "Тигр", 
    "Кролик", "Дракон", "Змія", "Кінь"
]

# 2003 - рік Кози, тобто початковий рік циклу
start_year = 2003
index = (year - start_year) % 12
zodiac = zodiac_animals[index]

print(f"Рік {year} відповідає знаку зодіаку: {zodiac}")
